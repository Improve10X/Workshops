package com.improve10x.workshops.handson.unittest.inventory.genericcode.v1;

// Product class representing individual products
class Product {
    private String name;
    private double price;

    public Product(String name, double price) {
        this.name = name;
        this.price = price;
    }

    public String getName() {
        return name;
    }

    public double getPrice() {
        return price;
    }
}

